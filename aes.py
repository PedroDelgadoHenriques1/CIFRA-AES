# Função para converter strings HEX em bytes
def hex_string_to_bytes(hex_string):
    return binascii.unhexlify(hex_string)

# Getter da S-BOX
def get_sbox_value(num):
    return sbox[num]

# Getter da S-BOX invertida
def get_sbox_invert(num):
    return rsbox[num]

# Rotaciona a palavra 8 bits para à esquerda
def rotate(word):
    return word[1:] + word[:1]

# Getter da tabela de R-CON
def get_rcon_value(num):
    # Garantir que num não ultrapasse o tamanho de Rcon
    if num >= len(Rcon):
        raise IndexError("Rcon index out of range.")
    return Rcon[num]


def core(word, iteration):
    word = rotate(word)
    word = [get_sbox_value(b) for b in word]
    # Adicionando verificação para o número de iterações
    if iteration < len(Rcon):  # Certifique-se de que não estamos fora do limite
        word[0] ^= get_rcon_value(iteration)
    else:
        print(f"Warning: Iteration {iteration} is out of range for Rcon.")
    return word


